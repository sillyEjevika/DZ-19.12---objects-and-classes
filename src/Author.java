public class Author {
    String authorName;
    String authorSurname;

    public Author() {
        this.authorName = authorName;
        this.authorSurname =  authorSurname;
    }

    public String getAuthorName() {
        return this.authorName;
    }
    public String getAuthorSurname() {
        return this.authorSurname;
    }
}
